@app.post("/chat", response_model=ChatResponse, tags=["Chat"])
async def chat(body: ChatRequest, request: Request):
    global _pending
    t0 = time.perf_counter()
    ip = request.client.host if request.client else "unknown"

    try:
        # Layer 1 — rate limit
        if not rate_limiter.is_allowed(ip):
            raise HTTPException(429, f"Rate limit: max {rate_limiter._max} req/min.")

        # Layer 2 — basic validation
        qv = validate_question(body.question)
        if not qv.valid:
            raise HTTPException(422, qv.error)

        logger.info(f"[CHAT] {ip}: {body.question!r}")

        # Layer 3 — profanity → hard reject
        if contains_profanity(body.question):
            _audit("REQUEST_REJECTED","BLOCKED","rejected","HIGH",
                   {"reason":"profanity","q":body.question[:80]})
            raise HTTPException(400,
                "Your query contains inappropriate language. Please rephrase.")

        # Layer 4 — ibtcode perception + validation
        p_result = _classify(body.question)
        intent, conf = p_result["intent"], p_result["confidence"]

        try:
            ctx_dict = ibt_context.to_dict()
        except AttributeError:
            ctx_dict = {
                "last_intent":    getattr(ibt_context, "last_intent", None),
                "last_timestamp": getattr(ibt_context, "last_timestamp", 0.0),
            }
        validation = validate_perception(
            {"intent": intent, "confidence": conf, "raw_input": body.question},
            context=ctx_dict,
        )
        v_status = validation.get("status", "VALID")
        logger.info(f"[VALIDATION] {v_status} intent={intent}")

        _audit("INTENT_DETECTED", intent, "processing", "LOW",
               {"question": body.question, "confidence": conf, "v_status": v_status})

        if v_status == "REJECT":
            _audit("REQUEST_REJECTED", intent, "rejected", "HIGH",
                   {"reason": validation.get("reason")})
            raise HTTPException(400, validation.get("message"))

        # CLARIFY — nonsense / off-topic / too-short
        if v_status == "CLARIFY":
            _audit("CLARIFICATION_NEEDED", intent, "clarify", "LOW",
                   {"reason": validation.get("reason")})
            return ChatResponse(
                question=body.question,
                message=validation.get("message",
                    "I couldn't understand that. Please ask a clinic-related question "
                    "(e.g. patients, doctors, appointments, revenue)."),
                intent=intent, risk="LOW", validation_status="CLARIFY",
                needs_confirmation=False, cached=False,
                latency_ms=int((time.perf_counter()-t0)*1000))

        q_lo    = body.question.lower()
        q_words = set(re.findall(r"\b\w+\b", q_lo))

        # Layer 5a — DROP → hard block
        if "drop" in q_words:
            _audit("DROP_BLOCKED","BLOCKED","rejected","CRITICAL",
                   {"question":body.question})
            raise HTTPException(400,
                "DROP is permanently blocked. This system is read-only "
                "except for confirmed DELETE/UPDATE.")

        # Layer 5b — DELETE / REMOVE / ERASE
        if q_words & {"delete", "remove", "erase"}:
            name = _extract_patient_name(body.question)
            if not name:
                return ChatResponse(
                    question=body.question,
                    message="Please specify the patient's full name. "
                            "Example: 'delete patient name Anjali Chopra'",
                    intent="DESTRUCTIVE_QUERY", risk="HIGH",
                    needs_confirmation=False, cached=False,
                    latency_ms=int((time.perf_counter()-t0)*1000))
            sql = (f"DELETE FROM patients "
                   f"WHERE LOWER(first_name||' '||last_name)=LOWER('{name}')")
            _pending = {"type":"DELETE","question":body.question,
                        "intent":"DESTRUCTIVE_QUERY","patient_name":name,
                        "sql":sql,"timestamp":time.time()}
            _audit("DELETE_CONFIRMATION_REQUIRED","DESTRUCTIVE_QUERY","pending","HIGH",
                   {"question":body.question,"patient_name":name})
            return ChatResponse(
                question=body.question,
                message=(f"⚠️ WARNING: You are about to DELETE patient '{name}'.\n"
                         f"This cannot be undone.\n"
                         f"POST /confirm {{\"confirm\": true}} to proceed, "
                         f"or {{\"confirm\": false}} to cancel."),
                sql_query=sql,
                intent="DESTRUCTIVE_QUERY", risk="HIGH",
                needs_confirmation=True, cached=False,
                latency_ms=int((time.perf_counter()-t0)*1000))

        # Layer 5c — UPDATE / MODIFY / CHANGE
        # FIX: generate SQL first, THEN decide whether to gate
        if q_words & {"update", "modify", "change"} or \
                re.search(r"\bupdate\b|\bmodify\b", q_lo):
            logger.info("[CHAT] UPDATE detected — generating SQL first")
            result = await _run_llm(body.question)
            gen_sql = result.get("sql_query", "")

            # If LLM returned SELECT (interpreted as a read query), just return it
            if gen_sql and gen_sql.strip().upper().startswith("SELECT"):
                logger.info("[CHAT] UPDATE keyword but LLM returned SELECT — safe")
                # fall through to normal return below with this result
            else:
                # Real UPDATE SQL — gate on confirmation
                _pending = {"type":"UPDATE","question":body.question,
                            "intent":"DESTRUCTIVE_QUERY","sql":gen_sql,
                            "result":result,"timestamp":time.time()}
                _audit("UPDATE_CONFIRMATION_REQUIRED","DESTRUCTIVE_QUERY","pending","HIGH",
                       {"question":body.question,"sql":gen_sql})
                return ChatResponse(
                    question=body.question,
                    message=(f"⚠️ WARNING: This will UPDATE data in the database.\n"
                             f"Review the SQL and confirm.\n"
                             f"POST /confirm {{\"confirm\": true}} to execute, "
                             f"or {{\"confirm\": false}} to cancel."),
                    sql_query=gen_sql,
                    intent="DESTRUCTIVE_QUERY", risk="HIGH",
                    needs_confirmation=True, cached=False,
                    latency_ms=int((time.perf_counter()-t0)*1000))

            # UPDATE keyword but SELECT sql — return normally (use result from above)
            latency = int((time.perf_counter()-t0)*1000)
            ibt_context.update(body.question, intent, "completed")
            _audit("QUERY_SUCCESS", intent, "completed", "LOW",
                   {"row_count": result.get("row_count"), "latency_ms": latency})
            return ChatResponse(
                question=body.question, cached=False, latency_ms=latency,
                intent=intent, risk="LOW", validation_status="VALID",
                needs_confirmation=False,
                **{k: result.get(k) for k in
                   ("message","sql_query","columns","rows","row_count")})

        # Layer 6 — cache
        cached = query_cache.get(body.question)
        if cached is not None:
            logger.info("[CHAT] cache hit")
            return ChatResponse(
                question=body.question, cached=True,
                latency_ms=int((time.perf_counter()-t0)*1000),
                intent=intent, risk="LOW", validation_status="VALID",
                needs_confirmation=False,
                **{k: cached[k] for k in
                   ("message","sql_query","columns","rows","row_count") if k in cached})

        # Layer 7 — LLM
        result = await _run_llm(body.question)

        # Layer 8 — sensitive column gate
        if result.get("sql_query"):
            cr = _col_risk(result["sql_query"])
            if cr["flag"]:
                _pending = {"type":"SENSITIVE","question":body.question,
                            "intent":intent,"sql":result["sql_query"],
                            "result":result,"timestamp":time.time()}
                _audit("SENSITIVE_DATA_CONFIRMATION_REQUIRED", intent, "pending",
                       "MEDIUM", {"question":body.question,"reason":cr["reason"]})
                return ChatResponse(
                    question=body.question,
                    message=(f"🔒 This query accesses sensitive patient data "
                             f"({cr['reason']}).\n"
                             f"POST /confirm {{\"confirm\": true}} to proceed."),
                    sql_query=result["sql_query"],
                    intent=intent, risk="MEDIUM",
                    needs_confirmation=True, cached=False,
                    latency_ms=int((time.perf_counter()-t0)*1000))

        latency = int((time.perf_counter()-t0)*1000)
        if result.get("sql_query") and result.get("rows") is not None:
            query_cache.set(body.question, result)

        ibt_context.update(body.question, intent, "completed")
        _audit("QUERY_SUCCESS", intent, "completed", "LOW",
               {"row_count": result.get("row_count"), "latency_ms": latency})

        return ChatResponse(
            question=body.question, cached=False, latency_ms=latency,
            intent=intent, risk="LOW", validation_status="VALID",
            needs_confirmation=False,
            **{k: result.get(k) for k in
               ("message","sql_query","columns","rows","row_count")})

    except HTTPException: raise
    except Exception as e:
        logger.error(f"[CHAT] {e}", exc_info=True)
        _audit("ERROR","UNKNOWN","error","HIGH",{"error":str(e),"q":body.question})
        raise HTTPException(500, f"Internal error: {str(e)[:200]}")


@app.post("/confirm", tags=["Chat"])
async def confirm(data: ConfirmRequest):
    global _pending
    if not _pending:
        return {"status": "no_pending", "message": "No pending action."}

    ptype  = _pending.get("type",   "unknown")
    intent = _pending.get("intent", "UNKNOWN")

    if not data.confirm:
        _audit("CONFIRMATION_CANCELLED", intent, "cancelled", "LOW",
               {"question": _pending.get("question"), "type": ptype})
        _pending = None
        return {"status": "cancelled", "message": "Operation cancelled."}

    # DELETE
    if ptype == "DELETE":
        name = _pending.get("patient_name", "")
        aff  = _delete_by_name(name)
        if aff == 0:
            aff = _delete_by_like(name)
        _audit("DELETE_EXECUTED","DESTRUCTIVE_QUERY","executed","HIGH",
               {"patient_name":name,"affected_rows":aff})
        _pending = None
        return {"status":"executed",
                "message":f"Deleted {aff} patient(s) matching '{name}'.",
                "affected_rows":aff}

    # UPDATE
    if ptype == "UPDATE":
        sql = _pending.get("sql")
        if not sql:
            _pending = None
            return {"status":"error","message":"No SQL was generated to execute."}
        _, _, _, aff = _execute_sql(sql)
        _audit("UPDATE_EXECUTED","DESTRUCTIVE_QUERY","executed","HIGH",
               {"sql":sql,"affected_rows":aff})
        _pending = None
        return {"status":"executed",
                "message":f"Update executed. {aff} row(s) affected.",
                "affected_rows":aff,"sql":sql}

    # SENSITIVE SELECT
    if ptype == "SENSITIVE":
        result = _pending.get("result", {})
        _audit("SENSITIVE_DATA_ACCESSED", intent, "executed","MEDIUM",
               {"question":_pending.get("question")})
        _pending = None
        return {"status":"executed","result":result}

    _pending = None
    return {"status":"error","message":f"Unknown pending type: {ptype}"}


