# ── DB helpers ─────────────────────────────────────────────────────────────────

def _db_path() -> str:
    return os.getenv("DB_PATH", "./clinic.db")


def _check_db() -> str:
    try:
        c = sqlite3.connect(_db_path()); c.execute("SELECT 1"); c.close()
        return "connected"
    except Exception:
        return "error"


def _execute_sql(sql: str) -> Tuple[Optional[List], Optional[List], int, int]:
    """Returns (cols, rows, row_count, affected_rows)."""
    try:
        conn = sqlite3.connect(_db_path())
        conn.row_factory = sqlite3.Row
        cur  = conn.cursor()
        cur.execute(sql)
        up = sql.strip().upper()
        if up.startswith("SELECT") or up.startswith("WITH"):
            rows = cur.fetchall()
            if rows:
                cols = [d[0] for d in cur.description]
                conn.close()
                return cols, [list(r) for r in rows], len(rows), 0
            conn.close()
            return None, None, 0, 0
        else:
            conn.commit()
            aff = cur.rowcount
            conn.close()
            return None, None, 0, aff
    except Exception as e:
        logger.error(f"[SQL] {e}")
        return None, None, 0, 0


def _delete_by_name(name: str) -> int:
    try:
        conn = sqlite3.connect(_db_path()); cur = conn.cursor()
        cur.execute(
            "DELETE FROM patients WHERE LOWER(first_name||' '||last_name)=LOWER(?)",
            (name,))
        conn.commit(); aff = cur.rowcount; conn.close()
        logger.info(f"[DELETE] exact '{name}' → {aff}")
        return aff
    except Exception as e:
        logger.error(f"[DELETE] exact: {e}"); return 0


def _delete_by_like(name: str) -> int:
    try:
        conn = sqlite3.connect(_db_path()); cur = conn.cursor()
        cur.execute(
            "DELETE FROM patients WHERE LOWER(first_name||' '||last_name) LIKE LOWER(?)",
            (f"%{name}%",))
        conn.commit(); aff = cur.rowcount; conn.close()
        logger.info(f"[DELETE] like '%{name}%' → {aff}")
        return aff
    except Exception as e:
        logger.error(f"[DELETE] like: {e}"); return 0


def _extract_patient_name(question: str) -> Optional[str]:
    patterns = [
        r"(?:delete|remove|erase)\s+patient\s+name\s+([A-Za-z\s]+)",
        r"(?:delete|remove|erase)\s+patient\s+([A-Za-z\s]+)",
        r"(?:delete|remove|erase)\s+([A-Za-z\s]+)\s+(?:from|patient)",
        r"(?:delete|remove|erase)\s+([A-Za-z\s]+)",
    ]
    noise = {"patient", "from", "the", "a", "an", "name"}
    for pat in patterns:
        m = re.search(pat, question, re.IGNORECASE)
        if m:
            parts = [w for w in m.group(1).strip().split()
                     if w.lower() not in noise]
            name = " ".join(parts).strip()
            if name:
                return name
    return None


def _classify(question: str) -> Dict:
    """ibtcode perception + keyword fallback."""
    try:
        p = detect_intent(question)
        intent, conf = p.get("intent", "UNKNOWN"), p.get("confidence", 0.5)
    except Exception:
        intent, conf = "UNKNOWN", 0.5

    if intent in ("UNKNOWN", None):
        q = question.lower()
        for mapped, kws in [
            ("PATIENT_QUERY",     ["patient","patients","registered","city","gender"]),
            ("DOCTOR_QUERY",      ["doctor","doctors","specialization","department"]),
            ("APPOINTMENT_QUERY", ["appointment","appointments","schedule","booking","visit","no-show"]),
            ("FINANCIAL_QUERY",   ["revenue","invoice","spending","cost","paid","total","amount","billing"]),
            ("SENSITIVE_QUERY",   ["phone","email","address","dob","birth","personal","contact"]),
            ("AGGREGATION_QUERY", ["count","average","sum","top","max","min","highest","percentage"]),
            ("TIME_QUERY",        ["month","year","date","quarter","trend","weekly","daily","last","past"]),
            ("TREATMENT_QUERY",   ["treatment","procedure","duration"]),
            ("COMPARISON_QUERY",  ["compare","versus","vs","department","breakdown"]),
        ]:
            if any(k in q for k in kws):
                return {"intent": mapped, "confidence": 0.85}
        return {"intent": "GENERAL_QUERY", "confidence": 0.6}

    return {"intent": intent, "confidence": conf}


def _col_risk(sql: str) -> Dict:
    lo = sql.lower()
    for col in SENSITIVE_COLUMNS:
        if re.search(rf"\b{col}\b", lo):
            return {"flag": True, "reason": f"Sensitive column accessed: {col}"}
    return {"flag": False}


def _audit(action, intent, state, risk, details=None):
    try:
        ibt_audit.log(action_name=action, intent=intent,
                      state=state, risk=risk, details=details or {})
        logger.info(f"[AUDIT] {action} | {intent} | {risk}")
    except Exception as e:
        logger.error(f"[AUDIT] {e}")


# ── LLM ────────────────────────────────────────────────────────────────────────

async def _run_llm(question: str) -> Dict:
    try:
        sql = get_agent().generate_sql(question)
        basic = validate_sql(sql)
        if not basic.valid:
            raise ValueError(basic.error)
        logger.info(f"[LLM] {sql[:200]}")
        cols, rows, cnt, aff = _execute_sql(sql)
        if cols and rows:
            return {"message": f"Found {cnt} result(s).", "sql_query": sql,
                    "columns": cols, "rows": rows, "row_count": cnt}
        if aff > 0:
            return {"message": f"{aff} row(s) affected.", "sql_query": sql,
                    "columns": None, "rows": None, "row_count": 0}
        return {"message": "No data found.", "sql_query": sql,
                "columns": None, "rows": None, "row_count": 0}
    except Exception as e:
        logger.error(f"[LLM] {e}")
        return await _fallback(question)


async def _fallback(question: str) -> Dict:
    logger.warning("[FALLBACK] rule-based")
    q = question.lower()
    rules = [
        (r"how many patients|total patients",
         "SELECT COUNT(*) AS total_patients FROM patients"),
        (r"top.{0,8}patients.{0,8}spend",
         "SELECT p.first_name||' '||p.last_name AS patient,SUM(i.total_amount) AS total "
         "FROM patients p JOIN invoices i ON p.id=i.patient_id "
         "GROUP BY p.id ORDER BY total DESC LIMIT 5"),
        (r"total revenue",
         "SELECT SUM(total_amount) AS revenue,SUM(paid_amount) AS collected,"
         "SUM(total_amount-paid_amount) AS outstanding FROM invoices"),
        (r"list.{0,6}doctors|show.{0,6}doctors",
         "SELECT name,specialization,department FROM doctors ORDER BY specialization"),
        (r"unpaid|overdue",
         "SELECT p.first_name||' '||p.last_name AS patient,"
         "i.total_amount,i.paid_amount,i.status FROM invoices i "
         "JOIN patients p ON p.id=i.patient_id "
         "WHERE i.status IN ('Pending','Overdue')"),
    ]
    for pat, sql in rules:
        if re.search(pat, q):
            cols, rows, cnt, _ = _execute_sql(sql)
            if rows:
                return {"message": f"Found {cnt} result(s). (fallback)",
                        "sql_query": sql, "columns": cols, "rows": rows, "row_count": cnt}
    return {"message": "Groq unavailable and no rule matched. Try again later.",
            "sql_query": None, "columns": None, "rows": None, "row_count": None}


